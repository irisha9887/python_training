    def fill_secondary_fields_for_contact(self, contact):
        wd = self.app.wd
        wd.find_element_by_name("address2").click()
        wd.find_element_by_name("address2").clear()
        wd.find_element_by_name("address2").send_keys(contact.address2)
        wd.find_element_by_xpath("//body").click()
        wd.find_element_by_name("phone2").click()
        wd.find_element_by_name("phone2").clear()
        wd.find_element_by_name("phone2").send_keys(contact.phone2)
        wd.find_element_by_name("notes").click()
        wd.find_element_by_name("notes").clear()
        wd.find_element_by_name("notes").send_keys(contact.notes)

    def create(self, contact):
        wd = self.app.wd
        # Create contact and fill all fields
        self.fill_primary_fields_for_contact(contact)
        wd.find_element_by_name("new_group").click()
        self.fill_secondary_fields_for_contact(contact)
        wd.find_element_by_xpath("//input[21]").click()
        self.contact_cache = None

    def modify_first_contact(self):
        self.modify_contact_by_index(0)

    def modify_contact_by_index(self, contact, index):
        wd = self.app.wd
        # Get list of all elements and select the right element
        wd.find_elements_by_css_selector("img[alt='Edit']")[index].click()
        # Fill contact form with new values
        self.fill_primary_fields_for_contact(contact)
        self.fill_secondary_fields_for_contact(contact)
        wd.find_element_by_name("update").click()
        self.contact_cache = None

    def delete_first_contact(self):
        self.delete_contact_by_index(0)

    def delete_contact_by_index(self,index):
        wd = self.app.wd
        # Select some contact
        wd.find_elements_by_name("selected[]")[index].click()
        # Submit deletion
        wd.find_element_by_xpath("//input[@value='Delete']").click()
        wd.switch_to_alert().accept()
        self.contact_cache = None

    def count(self):
        wd = self.app.wd
        return len(wd.find_elements_by_name("selected[]"))

    contact_cache = None

    def get_contact_list(self):
        if self.contact_cache is None:
            wd = self.app.wd
            self.app.navigation.open_home_page()
            self.contact_cache = []
            for element in wd.find_elements_by_name("entry"):
                cell_list = element.find_elements_by_tag_name("td")
                last_name = cell_list[1].text
                first_name = cell_list[2].text
                id = element.find_element_by_name("selected[]").get_attribute("value")
                self.contact_cache.append(Contact(lastname=last_name, firstname=first_name, id=id))
        return list(self.contact_cache)











